async function insertLongPost() {
  const { data, error } = await sb.from('posts').insert({
    title: 'تأملات في عصر الشاشات: تجربة قراءة دفترية طويلة',
    slug: 'long-reading-test-article',
    content: longContent,
    category: 'literature',
    cover_image: 'https://images.unsplash.com/photo-1455390582262-044cdead27d8?auto=format&fit=crop&q=80&w=900'
  });

  if (error) {
    if (error.code === '23505') {
        console.log('Post already exists, updating it...');
        await sb.from('posts').update({ content: longContent }).eq('slug', 'long-reading-test-article');
        console.log('Updated!');
    } else {
        console.error(error.message);
    }
  }
  else console.log('Long test article created successfully!');
}

insertLongPost();
