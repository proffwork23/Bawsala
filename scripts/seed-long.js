<p>ها نحن نقترب من نهاية هذا الاختبار النصي. نأمل أن تكون مسطرة الأسطر قد انتظمت بدقة متناهية تحت الكلمات، وأن تكون المحاذاة الواسعة قد أضفت على النص سحراً ورونقاً يليق باللغة العربية. اللغة التي طالما عُرفت بمرونتها وتناسبها مع الفنون البصرية، من خط عربي ساحر إلى طباعة عصرية حديثة تنسج حروفها بدقة بيكسلات الشاشة لتصل إلى عقل ووجدان القارئ.</p>
