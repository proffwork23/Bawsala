async function insertArtPost() {
  console.log('Inserting Art Post...');
  const { data, error } = await sb.from('posts').upsert({
    title: 'فلسفة الجمال وتداخل الفنون: من الزخرفة الإسلامية إلى الفن الرقمي',
    slug: 'philosophy-of-beauty-and-art',
    content: longArtContent,
    category: 'art',
    cover_image: 'https://images.unsplash.com/photo-1541701494587-cb58502866ab?auto=format&fit=crop&q=80&w=1200',
    created_at: new Date().toISOString()
  }, { onConflict: 'slug' });

  if (error) {
    console.error('Error inserting post:', error.message);
    process.exit(1);
  }
  
  console.log('Art post inserted successfully!');
}

insertArtPost();
